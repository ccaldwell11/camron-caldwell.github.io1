/**
* VARIABLES:
* 0. Variables serve as containers in memory, associating with various data types such as Numbers, Strings, Booleans, Arrays, Objects, etc.
*    They earn the name "variables" because their contents can be altered.
* 1. Creating a variable involves using the keyword 'var' followed by a chosen name.
* 2. There are two distinct stages in working with variables: declaration and initialization.
*/

// 1. declaration //
var myName; // At this point, myName is declared but lacks a defined value.
console.log(myName); // prints => undefined

// 2. initialization or assignment
myName = 'john'; // Assigning a value to myName.
console.log(myName); // prints => john

// 3. Re-assignment
myName = 'bob'; // Modifying the existing value of myName.
console.log(myName); // prints => bob

