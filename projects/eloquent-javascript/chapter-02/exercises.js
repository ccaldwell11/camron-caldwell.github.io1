function triangles(num) {
  for (let i = 1; i <= num; i++) {
    console.log('#'.repeat(i));
  }
}
////////////////////////////////////////////////////////////////////////////////
// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, stop){
  for(var i = start; i <= stop; i++){
    if(i % 3 === 0 && i % 5 === 0){
      console.log('fizzbuzz');
    }
    else if(i % 3 === 0){
      console.log('fizz');
    }
    else if(i % 5 === 0){
      console.log('buzz');
    }
    else{
      console.log(i);
    }
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  let chessBoard = '';
  for (let i = 0; i < num; i++) {
    for (let j = 0; j < num; j++) {
      if ((i + j) % 2 === 0) {
        chessBoard += ' ';
      } else {
        chessBoard += '#';
      }
    }
    chessBoard += '\n';
  }
  console.log(chessBoard);
}

